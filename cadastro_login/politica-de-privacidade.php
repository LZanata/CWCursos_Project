    <h2>2. Como Recolhemos os Dados Pessoais</h2>
    <ul>
        <li><strong>Cadastro:</strong> Quando o usuário cria uma conta na plataforma.</li>
        <li><strong>Uso do site:</strong> Através de interações com páginas, buscas, comentários, entre outros.</li>
